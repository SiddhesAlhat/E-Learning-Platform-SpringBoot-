package com.elearning.model;

/**
 * Enum representing different learning styles based on VARK model
 */
public enum LearningStyle {
    VISUAL, // Prefers images, diagrams, videos
    AUDITORY, // Prefers lectures, discussions, podcasts
    READING, // Prefers text-based learning
    KINESTHETIC, // Prefers hands-on, practical exercises
    MULTIMODAL // No dominant style, mixture of above
}
